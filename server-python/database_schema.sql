-- 0. Enable the pgvector extension first
CREATE EXTENSION IF NOT EXISTS vector;

-- 1. Create the 'agents' table
CREATE TABLE agents (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  name TEXT NOT NULL,
  system_prompt TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 2. Create the 'documents' table
CREATE TABLE documents (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  agent_id UUID REFERENCES agents(id) ON DELETE CASCADE,
  filename TEXT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 3. Create the 'document_embeddings' table

CREATE TABLE document_embeddings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  document_id UUID REFERENCES documents(id) ON DELETE CASCADE,
  content TEXT NOT NULL,
  embedding VECTOR(384) 
);



-- 1. Ensure RLS is enabled on the agents table
ALTER TABLE agents ENABLE ROW LEVEL SECURITY;

-- 2. Allow users to READ their own agents
CREATE POLICY "Users can view own agents" 
ON agents FOR SELECT 
TO authenticated 
USING (auth.uid() = user_id);

-- 3. Allow users to CREATE their own agents
CREATE POLICY "Users can create own agents" 
ON agents FOR INSERT 
TO authenticated 
WITH CHECK (auth.uid() = user_id);

-- 4. Allow users to DELETE their own agents (future-proofing)
CREATE POLICY "Users can delete own agents" 
ON agents FOR DELETE 
TO authenticated 
USING (auth.uid() = user_id);


